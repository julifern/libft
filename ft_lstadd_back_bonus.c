#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*tmp;

	tmp = *lst;
	while (tmp && tmp->next)
		tmp = tmp->next;
	if (*lst)
		tmp->next = new;
	else
		*lst = new;
}
